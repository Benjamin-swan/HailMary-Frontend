export function pickDohwaCopyKey(
  dohwa: CharmDohwaView,
  variantTags: CharmVariantTag[]
): DohwaCopyKey {
  if (!dohwa.present) {
    return variantTags.includes("hwagae_present") ? "absent_hwagae" : "absent";
  }
  if (variantTags.includes("dohwa_multi")) return "multi";
  return (dohwa.pillar ?? "day") as DohwaCopyKey;
}
